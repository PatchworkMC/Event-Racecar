package net.minecraftforge.eventbus.api;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Used in Forge's EventBus as a magic annotation to subscribe to an event, this is just a marker for an optional Patchwork Patcher artifact.
 * @deprecated This annotation should not be used by hybrid mods as it does nothing.
 */
@Retention(value = RetentionPolicy.RUNTIME)
@Target(value = ElementType.METHOD)
@Deprecated
public @interface SubscribeEvent {
}
